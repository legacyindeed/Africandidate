// Helper function to format currency
export const formatCurrency = (amount) => {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
    maximumFractionDigits: 0
  }).format(amount);
};

// Helper function to get tuition tier
export const getTuitionTier = (tuition) => {
  if (tuition < 100000) return { label: 'Value', color: '#10B981' };
  if (tuition < 140000) return { label: 'Moderate', color: '#3B82F6' };
  if (tuition < 160000) return { label: 'Premium', color: '#F59E0B' };
  return { label: 'Top Tier', color: '#8B5CF6' };
};
